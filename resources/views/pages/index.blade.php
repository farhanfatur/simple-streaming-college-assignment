@section("javascript")
<script>
  var idAsset = 0
  function ShowModalDelete(id) {
    idAsset = id
  }

  function DeleteAsset() {
    $.ajax({
      headers: {
        'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content')
      },
      url: "assets/"+idAsset,
      type: 'DELETE',
      success: function(result) {
          window.location.reload()
      }
    })
  }
</script>
